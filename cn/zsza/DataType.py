# 字符串和元组是不可变的,而列表是可变（mutable）的,可以对它进行随意修改
# 我们可以将字符串和元组转换成一个列表,只需使用list函数
# 1、列表:用中括号表示（list）
nums = [1, 2, 3, 4, 5]
print('第一个元素:', nums[0])
print('最后一个元素:', nums[-1])

# 2、字符串（string）
s = 'abcdef'
print('字符串首个字母:', s[0])

